public class ExemploForArrays {
    public static void main(String[]args){

        // Estrutura de Arrys (o indice inicia em ZERO)
        String alunos [] = {"Diego", "Bruna", "Neide", "Vanessa"};

        // inicializacao com variavel; condicao com .determinacao; incrementacao
        for (int x=0; x<alunos.length ; x++){
            System.out.println(" O aluno no índice x = " + x + " é " + alunos[ x ]);
        }

    }
    
}
